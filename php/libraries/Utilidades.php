    public static function FillZero($campo, $digitos)
    {
        return str_pad($campo, $digitos, '0', STR_PAD_LEFT);
    }

    public static function FormatMoney($number, $fractional = false)
    {
        if ($fractional) {
            $number = sprintf('%.2f', $number);
        }
        while (true) {
            $replaced = preg_replace('/(-?\d+)(\d\d\d)/', '$1.$2', $number);
            if ($replaced != $number) {
                $number = $replaced;
            } else {
                break;
            }
        }
        return $number;
    }

    public static function CadenaAleatoria($arrayCharacters = [], $length = 8){
        // 0 mayusculas
        // 1 minusculas
        // 2 numeros
        // 3 caracteres especiales
        // sin array de caracteres, combinado de los anteriores
        $permitted_chars = '';
        if( is_array($arrayCharacters) && count($arrayCharacters) ){
            foreach ($arrayCharacters as $character) {
                switch ($character) {
                    case 0:
                        $permitted_chars .= 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
                        break;
                    case 1:
                        $permitted_chars .= 'abcdefghijklmnopqrstuvwxyz';
                        break;
                    case 2:
                        $permitted_chars .= '0123456789';
                        break;
                    case 3:
                        $permitted_chars .= '¡!#$%&/()=¿?-_*+';
                        break;
                }
            }
        }else{
            $permitted_chars = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ¡!#$%&/()=¿?-_*+';
        }
        
        return substr(str_shuffle($permitted_chars), 0, $length);
    }

    public static function ipReal()
    {
        if (isset($_SERVER["HTTP_CLIENT_IP"])) {
            return $_SERVER["HTTP_CLIENT_IP"];
        } elseif (isset($_SERVER["HTTP_X_FORWARDED_FOR"])) {
            return $_SERVER["HTTP_X_FORWARDED_FOR"];
        } elseif (isset($_SERVER["HTTP_X_FORWARDED"])) {
            return $_SERVER["HTTP_X_FORWARDED"];
        } elseif (isset($_SERVER["HTTP_FORWARDED_FOR"])) {
            return $_SERVER["HTTP_FORWARDED_FOR"];
        } elseif (isset($_SERVER["HTTP_FORWARDED"])) {
            return $_SERVER["HTTP_FORWARDED"];
        } else {
            return $_SERVER["REMOTE_ADDR"];
        }
    }
